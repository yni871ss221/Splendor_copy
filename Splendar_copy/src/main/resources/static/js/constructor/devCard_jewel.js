class DevCardJewel {
    
    constructor(){
        this.emerald = [];
        this.sapphire = [];
        this.ruby = [];
        this.diamond = [];
        this.onyx = [];
        this.goldJoker = [];
    }

    get _emerald(){
        return this.emerald;
    }
    set _emerald(emerald){
        this.emerald = emerald;
    }

    get _sapphire(){
        return this.sapphire;
    }
    set _sapphire(ssapphire){
        this.sapphire = sapphire;
    }

    get _ruby(){
        return this.ruby;
    }
    set _ruby(ruby){
        this.ruby = ruby;
    }

    get _diamond(){
        return this.diamond;
    }
    set _diamond(diamond){
        this.diamond = diamond;
    }

    get _onyx(){
        return this.onyx;
    }
    set _onyx(onyx){
        this.onyx = onyx;
    }

    get _goldJoker(){
        return this.goldJoker;
    }
    set _goldJoker(goldJoker){
        this.goldJoker = goldJoker;
    }
}