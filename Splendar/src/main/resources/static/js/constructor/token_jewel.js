class TokenJewel {
    
    constructor(){
        this.emerald = 0;
        this.sapphire = 0;
        this.ruby = 0;
        this.diamond = 0;
        this.onyx = 0;
        this.goldJoker = 0;
    }

    get _emerald(){
        return this.emerald;
    }
    set _emerald(emerald){
        this.emerald = emerald;
    }

    get _sapphire(){
        return this.sapphire;
    }
    set _sapphire(sapphire){
        this.sapphire = sapphire;
    }

    get _ruby(){
        return this.ruby;
    }
    set _ruby(ruby){
        this.ruby = ruby;
    }

    get _diamond(){
        return this.diamond;
    }
    set _diamond(diamond){
        this.diamond = diamond;
    }

    get _onyx(){
        return this.onyx;
    }
    set _onyx(onyx){
        this.onyx = onyx;
    }

    get _goldJoker(){
        return this.goldJoker;
    }
    set _goldJoker(goldJoker){
        this.goldJoker = goldJoker;
    }
}